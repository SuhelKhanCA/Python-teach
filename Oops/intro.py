# Class

class Human:

    # data members
    name = None
    height = None
    weight = None
    
    # methods
    def walk(self):
        print(self.name, "is walking")

zainab = Human()
zainab.name = "Zainab Zamir"
zainab.height = 160
zainab.weight = 50

print(zainab.name)
print(zainab.height)
print(zainab.weight)
zainab.walk()

